const ROTATION_START_KEY = "tw_tips_rotation_start_v1";

export function resetTipsRotationStart() {
  localStorage.removeItem(ROTATION_START_KEY);
}

export function getActiveTipsDatabase(now: Date = new Date()): TipsDatabase {
  let start = Number(localStorage.getItem(ROTATION_START_KEY) || "0");
  if (!start || Number.isNaN(start)) {
    start = now.getTime();
    localStorage.setItem(ROTATION_START_KEY, String(start));
  }

  const msPerDay = 24 * 60 * 60 * 1000;
  const daysSince = Math.floor((now.getTime() - start) / msPerDay);
  const idx = Math.floor(daysSince / 7) % DB.length;

  return DB[idx] || DB[0];
}

